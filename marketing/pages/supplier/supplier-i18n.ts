function getBrowserLocale(): string | null {
  if (typeof navigator === "undefined") return null;
  const nav: any = navigator as any;
  return (
    (Array.isArray(nav.languages) && nav.languages[0]) ||
    nav.language ||
    nav.userLanguage ||
    null
  );
}

