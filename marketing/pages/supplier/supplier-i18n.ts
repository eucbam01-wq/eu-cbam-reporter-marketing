export function useSupplierI18n(localeOverride?: string | null) {
  const [browserLocale, setBrowserLocale] = useState<string | null>(null);

  useEffect(() => {
    setBrowserLocale(getBrowserLocale());
  }, []);

  const locale: SupplierLocale = useMemo(() => {
    const picked = localeOverride || browserLocale;
    return normalizeLocale(picked);
  }, [localeOverride, browserLocale]);

  const t = useMemo(() => {
    const dict = TRANSLATIONS[locale] || TRANSLATIONS.en;
    return (key: string, vars?: Record<string, string>) => {
      const template = dict[key] ?? TRANSLATIONS.en[key] ?? key;
      if (!vars) return template;
      return Object.keys(vars).reduce((acc, k) => acc.replaceAll(`{{${k}}}`, vars[k]), template);
    };
  }, [locale]);

  return { locale, t };
}

